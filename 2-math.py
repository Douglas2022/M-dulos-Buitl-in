import math
# Acessar o número PI
print(math.pi)
print(f"{math.pi:.2f}")
print("---------------------------------------")
# Acessar o número de Euler
print(math.e)
print(f"{math.e:.2f}")
print("---------------------------------------")
# Arredondando o número para cima e para baixo.
num = 10.4
print(math.ceil(num))
print(math.floor(num))
print("---------------------------------------")
# Calcular um fatorial de um número
num1 = int(input("Digite um número: \n"))
print(math.factorial(num1))
print("---------------------------------------")
# Calcular um fatorial de um números
print(math.pow(5,5))
print("---------------------------------------")
# Raiz quadrada de um número
print(math.sqrt(169))
print("---------------------------------------")
# MDC
mdc = math.gcd(20,100)
print(mdc)
print("---------------------------------------")
# Logaritimo
print(math.log(10))